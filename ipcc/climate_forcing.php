<p>In order to perform the experiments called for in support of the IPCC's Fourth Assessment Report, models require certain so-called "forcing" datasets.  The forcing is mainly given in terms of atmospheric concentrations of greenhouse gases, ozone and aerosols. Although the atmospheric concentrations are based on emissions scenarios developed by IPCC, concentrations do not uniquely follow from emissions. Different concentrations can be derived from the same emissions scenarios, depending on the model used to derive them.</p>

<p>One set of concentrations that may be used to drive climate simulations can be obtained from Dr. Curt Covey (<a href="mailto:covey1@llnl.gov">covey1@llnl.gov</a>).  Use of this dataset is not mandatory.</p>

