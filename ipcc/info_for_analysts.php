<b><a name="model_names">Official 
model names</a></b></p>
<p>
To minimize confusion and maximize uniformity across analyses, authors are urged 
to use the official
<a href="http://www-pcmdi.llnl.gov/ipcc/model_documentation/ipcc_model_documentation.php">CMIP3 model 
&quot;I.D.'s&quot;</a> when referring to individual model results.&nbsp; In addition it 
would usually be appropriate to include a table containing the names of the 
modeling groups along with citations to their relevant papers.</p>
