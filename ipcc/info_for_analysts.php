<b><a name="Proper_acknowledgement">Proper citation and acknowledgement</a></b></p>
<p>In first making reference to the data obtained from the CMIP3 archive, please 
include the phrase &quot;the World Climate Research Programme's (WCRP's) Coupled Model 
Intercomparison Project phase 3 (CMIP3) multi-model dataset&quot;.&nbsp; Subsequent 
references within the same publication might refer to the data with terms such 
as &quot;CMIP3 data&quot;, &quot;the CMIP3 multi-model dataset&quot;, &quot;the CMIP3 archive&quot;, or the 
&quot;CMIP3 dataset&quot;.</p>
<p>&nbsp;Whenever you publish research based on model output from the CMIP3 
database, you should include the following acknowledgement:</p>
<p>&quot;<i>We acknowledge the modeling groups, the Program for Climate Model Diagnosis and 
Intercomparison (PCMDI) and the WCRP's Working Group on Coupled Modelling (WGCM) for their 
roles in making available the WCRP CMIP3 multi-model dataset.&nbsp;  Support of this dataset is 
provided by the Office of Science, U.S. Department of Energy.</i>&quot; </p>
<p> [Note that the acronyms, WCRP and CMIP3, should have already appeared in the main text of your article, 
where they were presumably defined.  You may also have already defined "PCMDI" and "WGCM", 
which would allow you to simply refer to them here using the acronyms.]</p>
<p> You may also find it useful to reference the following article, which provides an
overview and documentation of the CMIP3 modelling activity: </p>
<p> Meehl, G. A., C. Covey,  T. Delworth, M. Latif, B. McAvaney, J. F. B. Mitchell, 
R. J. Stouffer, and K. E. Taylor, 2007: The WCRP CMIP3 multi-model dataset:  A new era in climate 
change research,<i>Bulletin of the American Meteorological Society,</i> <b>88</b>, 1383-1394.</p>
