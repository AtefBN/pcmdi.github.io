<b><a name="Check_errata">Check errata page </a></b></p>
<p>
In some cases data contributed to the database have been found to contain errors 
of various sorts. Certain files are then withdrawn or replaced and an entry is 
logged on the errata page:
<a class="moz-txt-link-freetext" href="https://esg.llnl.gov:8443/about/errata.do">
https://esg.llnl.gov:8443/about/errata.do</a> <br>
&nbsp;The errata page should be checked frequently to avoid analyzing &quot;garbage&quot;, 
and to determine whether any of the files you have downloaded from the PCMDI 
site need to be updated or removed. If any of you find undetected errors in the 
data you are analyzing, please contact the responsible modeling group and copy
<a href="mailto:taylor13@llnl.gov">taylor13@llnl.gov</a>. </p>
<p>
One general warning concerning the global attributes in each file -- the &quot;title&quot; 
and &quot;comment&quot; attributes may in some cases incorrectly identify the experiment, 
whereas the &quot;experiment_id&quot; will always be correct.&nbsp; If there is a 
conflict, rely only on experiment_id.</p>
